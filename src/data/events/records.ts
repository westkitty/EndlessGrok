import type { EventCategory, EventSeverity, StarsilkEventRecord } from './types';

function event(
  assetId: string,
  category: EventCategory,
  title: string,
  body: string,
  severity: EventSeverity,
  sourceLabel: string,
  triggerCondition: string,
  affectedScope: string,
  mechanicalEffect: string,
  options: StarsilkEventRecord['options'],
): StarsilkEventRecord {
  return {
    id: assetId,
    assetId,
    category,
    title,
    body,
    severity,
    sourceLabel,
    triggerCondition,
    affectedScope,
    mechanicalEffect,
    options,
    testId: assetId,
    testFixture: `tests/fixtures/events/${assetId}.json`,
    status: 'prompted',
  };
}

