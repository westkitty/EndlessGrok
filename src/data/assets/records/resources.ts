import type { AssetRecord } from '../types';

function baseResource(
  id: string,
  mechanicalKey: string,
  displayName: string,
  mechanical: string,
  lore: string,
  iconName: AssetRecord['iconName'],
  testId: string,
  status: AssetRecord['status'] = 'integrated',
  extra?: Partial<AssetRecord>,
): AssetRecord {
  return {
    id,
    mechanicalKey,
    displayName,
    family: 'resources',
    states: ['default'],
    mechanicalMeaning: mechanical,
    loreMeaning: lore,
    accessibilityLabel: displayName,
    tooltip: {
      title: displayName,
      mechanical,
      lore,
      canonLabel: 'canon_faithful',
    },
    testId,
    sourceBasis: 'mechanical UI necessity',
    status,
    iconName,
    plannedFiles: {
      svg: `src/assets/icons/resources/${id}.svg`,
    },
    ...extra,
  };
}

